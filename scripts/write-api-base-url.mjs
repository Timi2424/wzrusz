import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const url = process.env.NX_API_URL ?? '';
const target = resolve(
  'apps/web/src/app/core/api/api-base-url.generated.ts',
);

const contents = `/** Generated by scripts/write-api-base-url.mjs — do not edit by hand. */
export const WZ_API_BASE_URL = ${JSON.stringify(url)};
`;

writeFileSync(target, contents, 'utf8');
console.log(`Wrote ${target} (NX_API_URL=${url ? '[set]' : '[empty]'})`);
